export const getRecipesByCategory = (category: Recipe["category"]): Recipe[] => {
  return recipeDatabase.filter(recipe => recipe.category === category);
};

export const getRecipesByProteinSource = (source: Recipe["proteinSource"]): Recipe[] => {
  return recipeDatabase.filter(recipe => recipe.proteinSource === source);
};

export const getRecipeById = (id: string): Recipe | undefined => {
  return recipeDatabase.find(recipe => recipe.id === id);
};

export const searchRecipes = (query: string): Recipe[] => {
  const lowerQuery = query.toLowerCase();
  return recipeDatabase.filter(recipe => 
    recipe.name.toLowerCase().includes(lowerQuery) ||
    recipe.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    recipe.ingredients.some(ing => ing.toLowerCase().includes(lowerQuery)) ||
    recipe.proteinSource.toLowerCase().includes(lowerQuery)
  );
};
